			<h2>Ms. Brown's Chocolate goes bigger with newest chocolate product</h2>
			<img class="news-img" src="img/choco.jpg">
			Ms. Brown is going big with its iconic chocolate bars for the holidays.<br><br>
			The chocolate maker announced Tuesday that it's selling a new product in the U.S. market known as Brown's Chocolate Box Deluxe, a chocolate that is double the size of the standard, diminutive Chocolate bar. It has a hazelnut center and is flecked with rice crisps.<br><br>
			The new product is expected to be on store shelves across the USA by Nov. 5.<br><br>
			Company officials called the new product the biggest innovation of the brand since they introduced Chocolate Bars to the market 25 years ago.<br><br>
			The Deluxe product was initially introduced in 2013 in China, where the company was trying to find a chocolate that was less sweet and creamier to appeal to the Chinese consumers' taste. The product was an immediate hit and helped drive $100 million in sales of chocolates in China last year. The Deluxe brand chocolates are wrapped in gold wrapper instead of silver.<br><br>
			The American version will have rice crisps instead of wheat crisps, allowing it to be marketed as gluten-free.<br><br>
			The Deluxe brand will be sold through Valentine's Day and will come in packages of three ($1.00), four ($1.29) as well as gift sizes of 15 ($3.99), 35 ($10.99) and 75 ($32.00). The 75-count gift box will only be sold online. The giant candy maker, which also produces brands like ABC and chocos, is marketing the Deluxe Box as a holiday gift.
